// Puzzle Management — это проект, предоставляющий по подписке комплект из 18 и более обучающих настольных игр, нацеленных на развитие навыков менеджмента, коммуникации, планирования и выполнения задач с помощью геймификации.

// Какова цель этих игр?
// Главная цель настольных игр  проекта Puzzle Management - обучение с применением геймификации, и помощь в развитии управленческих  компетенций, включая стратегическое планирование, эффективное лидерство, коммуникацию и управление проектами.

// Обучающие игры также дают возможность на практике опробовать ключевые элементы управления, планирования и реализации проектов.

// Как доступен комплект игр?
// Puzzle Management предлагает несколько вариантов доступа к играм:

